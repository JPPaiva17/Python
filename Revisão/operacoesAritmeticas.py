# Adição

adicao = 10 + 10
print('Adicao: 10 + 10 = ', adicao)

# Subtração

subtracao = 10 - 5
print('Subtracao: 10 - 5 = ', subtracao)

# Multiplicação

mult = 10 * 10
print('Multiplicacao: 10 * 10 = ', mult)

# Divisão

div = 9 / 2 
print('Divisao: 9 / 2 = ', div)

div2 = 9//2 # irá me dar só a parte inteira da divisão
print('Divisao parte Inteira: 9 // 2 = ', div2)


div3 = 9%2 # Modulo -> irá me dar só o resto da divisão
print('Modulo : 9 % 2 = ', div3)

# Potenciação

pot = 2**4
print('Potenciacao: 2**4 = ', pot)

